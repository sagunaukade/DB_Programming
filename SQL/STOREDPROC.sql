CREATE PROCEDURE SP_EMP2(@EMP_ID INT,@PF INT OUT, @PT INT OUT)
AS
BEGIN
DECLARE @SAL INT
SELECT @SAL=SALARY FROM Employees WHERE EMP_ID=@EMP_ID
SET @PF=@SAL*0.1
SET @PT=@SAL*0.2
END

---OUTPUT
DECLARE @bPF INT
DECLARE @bPT INT
EXEC SP_EMP2 2,@bPF OUT,@bPT OUT
PRINT 'PROVIDENT FUND IS:-' + CAST( @bPF AS VARCHAR)
PRINT 'PROFESSIONAL TAX IS:-' + CAST( @bPT AS VARCHAR)

